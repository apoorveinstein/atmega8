#include<avr/io.h>
#include<util/delay.h>

void main()
{
DDRD=0b11111111;
DDRC=0b0000111;
while(1)
{
PORTD=0b11000000;//0
_delay_ms(1000);
PORTD=0b11111001;//1
_delay_ms(1000);
PORTD=0b10100100;//2
PORTC=0b0000001;
_delay_ms(1000);
PORTD=0b10110000;//3
_delay_ms(1000);
PORTD=0b10011001;//4
_delay_ms(1000);
PORTD=0b10010010;//5
PORTC=0b0000010;
_delay_ms(1000);
PORTD=0b10000010;//6
_delay_ms(1000);
PORTD=0b11111000;//7
_delay_ms(1000);
PORTD=0b10000000;//8
PORTC=0b0000100;
_delay_ms(1000);
PORTD=0b10010000;//9
_delay_ms(1000);
PORTC=0b0000111;
}
}
